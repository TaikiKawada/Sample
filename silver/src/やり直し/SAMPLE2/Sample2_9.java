package やり直し.SAMPLE2;

public class Sample2_9 {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		
		//関係演算子→trueかfalseを返す
		System.out.println("a==b:"+(a==b));
		System.out.println("a!=b:"+(a!=b));
		//aとbが等しくない場合trueを返す
		System.out.println("a>b:"+(a>b));
		System.out.println("a>=b:"+(a>=b));
		System.out.println("a<b:"+(a<b));
		System.out.println("a<=b:"+(a<=b));
	}
}
